class GitHubAgent(SwarmAgent):
    """
    GitHub integration as a swarm agent.
    Listens on #github channel and executes operations.
    Auto-commits elite KAIROS cycles.
    """
    ROLE        = "github"
    DESCRIPTION = "GitHub automation: commit · push · PR · sync"
    CHANNELS    = ["#github", "#broadcast"]

    def __init__(self):
        super().__init__("GITHUB")
        self._gh      = GitHubClient()
        self._git     = LocalGit()
        self._commits = 0
        self._pushes  = 0

    async def on_message(self, msg: SwarmMessage):
        content_low = msg.content.lower()

        if msg.msg_type == MsgType.TASK or msg.msg_type == MsgType.GITHUB:
            if "commit" in content_low:
                await self._do_commit(msg)
            elif "push" in content_low:
                await self._do_push(msg)
            elif "sync" in content_low or "memory" in content_low:
                await self._sync_memory(msg)
            elif "status" in content_low:
                await self._repo_status(msg)

        # Auto-commit elite KAIROS cycles
        if msg.msg_type == MsgType.KAIROS and msg.metadata.get("is_elite"):
            await self._auto_commit_elite(msg)

    async def _do_commit(self, msg: SwarmMessage):
        commit_msg = msg.metadata.get("commit_msg", msg.content)
        await self.think(f"GITHUB: Committing — '{commit_msg[:50]}'")
        result = self._git.commit_and_push(commit_msg)
        self._commits += 1
        await self.say(
            f"{'✅' if result['ok'] else '❌'} Commit: {result.get('msg', result.get('error'))}",
            channel="#github",
            msg_type=MsgType.GITHUB,
            result=result,
        )

    async def _do_push(self, msg: SwarmMessage):
        files = msg.metadata.get("files", {})
        commit_msg = msg.metadata.get("commit_msg", "🖤 GH05T3 auto-push")
        if not files:
            await self.say("No files provided for push", channel="#github", msg_type=MsgType.ERROR)
            return
        try:
            url = await self._gh.push_files(files, commit_msg)
            self._pushes += 1
            await self.say(
                f"✅ Pushed {len(files)} files → {url}",
                channel="#github", msg_type=MsgType.GITHUB,
                commit_url=url,
            )
        except Exception as e:
            await self.say(f"❌ Push failed: {e}", channel="#github", msg_type=MsgType.ERROR)

    async def _sync_memory(self, msg: SwarmMessage):
        """Push Memory Palace snapshot to GitHub."""
        mem_path = Path("memory/palace.db")
        if not mem_path.exists():
            await self.say("Memory DB not found", channel="#github", msg_type=MsgType.ERROR)
            return

        # Export conversation log
        conv_path = Path("memory/conversations.jsonl")
        files = {}
        if conv_path.exists():
            files["memory/conversations.jsonl"] = conv_path.read_text()

        kairos_path = Path("evolution/kairos_log.jsonl")
        if kairos_path.exists():
            files["evolution/kairos_log.jsonl"] = kairos_path.read_text()

        if files:
            try:
                url = await self._gh.push_files(
                    files,
                    f"🧠 [GH05T3] Memory sync — {time.strftime('%Y-%m-%d %H:%M')}"
                )
                await self.say(
                    f"✅ Memory synced to GitHub: {url}",
                    channel="#github", msg_type=MsgType.GITHUB,
                )
            except Exception as e:
                await self.say(f"❌ Memory sync failed: {e}", channel="#github", msg_type=MsgType.ERROR)

    async def _repo_status(self, msg: SwarmMessage):
        try:
            info = await self._gh.repo_info()
            await self.say(
                f"📊 Repo: {info.get('name')} | ⭐ {info.get('stars')} | "
                f"🍴 {info.get('forks')} | Issues: {info.get('open_issues')}",
                channel="#github", msg_type=MsgType.GITHUB,
                repo_info=info,
            )
        except Exception as e:
            await self.say(f"Repo status unavailable: {e}", channel="#github", msg_type=MsgType.ERROR)

    async def _auto_commit_elite(self, msg: SwarmMessage):
        """Auto-commit elite KAIROS cycle."""
        cycle_id = msg.metadata.get("cycle_id", "?")
        score    = msg.metadata.get("score", 0.0)
        commit_msg = f"⚡ [KAIROS Elite #{cycle_id}] score={score:.2f} — auto-committed"
        self._git.add_all()
        self._git.commit(commit_msg)
        await self.say(
            f"Elite cycle #{cycle_id} (score {score:.2f}) committed to GitHub",
            channel="#github", msg_type=MsgType.GITHUB,
        )

    @property
    def stats(self) -> dict:
        base = super().stats
        return {**base, "commits": self._commits, "pushes": self._pushes}

    async def close(self):
        await self._gh.close()


