class LocalGit:
    """
    Wraps local git CLI for fast commits without API overhead.
    Used when TatorTot has the repo cloned locally.
    """

    def __init__(self, repo_path: str = None):
        self.repo = Path(repo_path or os.environ.get("GH05T3_REPO_PATH", ".")).resolve()

    def _run(self, *args) -> tuple[bool, str]:
        result = subprocess.run(
            ["git"] + list(args),
            cwd=self.repo,
            capture_output=True,
            text=True,
        )
        return result.returncode == 0, result.stdout + result.stderr

    def status(self) -> str:
        _, out = self._run("status", "--short")
        return out

    def add_all(self) -> bool:
        ok, _ = self._run("add", "-A")
        return ok

    def commit(self, message: str) -> tuple[bool, str]:
        return self._run("commit", "-m", message)

    def push(self, remote: str = "origin", branch: str = "main") -> tuple[bool, str]:
        return self._run("push", remote, branch)

    def pull(self) -> tuple[bool, str]:
        return self._run("pull", "--rebase")

    def commit_and_push(self, message: str,
                         branch: str = "main") -> dict:
        self.add_all()
        ok_c, out_c = self.commit(message)
        if not ok_c:
            if "nothing to commit" in out_c:
                return {"ok": True, "msg": "nothing to commit", "output": out_c}
            return {"ok": False, "error": out_c}
        ok_p, out_p = self.push(branch=branch)
        return {
            "ok":    ok_p,
            "msg":   f"Pushed to {branch}",
            "output": out_c + out_p,
        }


