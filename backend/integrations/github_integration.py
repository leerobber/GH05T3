# ─────────────────────────────────────────────
# GITHUB CLIENT
# ─────────────────────────────────────────────

class GitHubClient:
    """
    Async GitHub REST API v3 client.
    Uses PAT auth. Supports file create/update, PRs, issues.
    """

    def __init__(self, pat: str = None, repo: str = None):
        self._pat  = pat or os.environ.get("GITHUB_PAT", "")
        self._repo = repo or os.environ.get("GITHUB_REPO", "leerobber/GH05T3")
        self._http = httpx.AsyncClient(
            base_url=GITHUB_API,
            headers={
                "Authorization":        f"token {self._pat}",
                "Accept":               "application/vnd.github.v3+json",
                "X-GitHub-Api-Version": "2022-11-28",
            },
            timeout=20.0,
        )

    # ── FILE OPS ──────────────────────────────

    async def get_file(self, path: str, branch: str = "main") -> Optional[dict]:
        try:
            r = await self._http.get(f"/repos/{self._repo}/contents/{path}",
                                      params={"ref": branch})
            if r.status_code == 200:
                return r.json()
        except Exception as e:
            log.error(f"[GH] get_file {path}: {e}")
        return None

    async def put_file(self, path: str, content: str, message: str,
                       branch: str = "main") -> dict:
        """Create or update a file in the repo."""
        encoded = base64.b64encode(content.encode()).decode()
        existing = await self.get_file(path, branch)
        sha = existing.get("sha") if existing else None

        payload = {
            "message": message,
            "content": encoded,
            "branch":  branch,
        }
        if sha:
            payload["sha"] = sha

        r = await self._http.put(f"/repos/{self._repo}/contents/{path}", json=payload)
        r.raise_for_status()
        return r.json()

    async def delete_file(self, path: str, message: str, branch: str = "main") -> dict:
        existing = await self.get_file(path, branch)
        if not existing:
            return {"error": "file not found"}
        r = await self._http.delete(
            f"/repos/{self._repo}/contents/{path}",
            json={"message": message, "sha": existing["sha"], "branch": branch}
        )
        r.raise_for_status()
        return r.json()

    # ── COMMITS / TREE ────────────────────────

    async def get_latest_commit(self, branch: str = "main") -> dict:
        r = await self._http.get(f"/repos/{self._repo}/branches/{branch}")
        r.raise_for_status()
        return r.json()["commit"]

    async def create_tree(self, files: dict[str, str], base_sha: str) -> str:
        """
        Create a git tree from {path: content} dict.
        Returns new tree SHA.
        """
        tree = []
        for path, content in files.items():
            tree.append({
                "path":    path,
                "mode":    "100644",
                "type":    "blob",
                "content": content,
            })
        r = await self._http.post(f"/repos/{self._repo}/git/trees",
                                   json={"base_tree": base_sha, "tree": tree})
        r.raise_for_status()
        return r.json()["sha"]

    async def create_commit(self, tree_sha: str, parent_sha: str,
                             message: str) -> str:
        """Create a commit. Returns commit SHA."""
        r = await self._http.post(
            f"/repos/{self._repo}/git/commits",
            json={
                "message": message,
                "tree":    tree_sha,
                "parents": [parent_sha],
            }
        )
        r.raise_for_status()
        return r.json()["sha"]

    async def update_ref(self, commit_sha: str, branch: str = "main"):
        """Fast-forward branch ref to commit SHA."""
        r = await self._http.patch(
            f"/repos/{self._repo}/git/refs/heads/{branch}",
            json={"sha": commit_sha, "force": False}
        )
        r.raise_for_status()

    async def push_files(self, files: dict[str, str], message: str,
                          branch: str = "main") -> str:
        """
        Atomic multi-file push. Returns commit URL.
        files: {repo_path: file_content}
        """
        head  = await self.get_latest_commit(branch)
        tree  = await self.create_tree(files, head["commit"]["tree"]["sha"])
        sha   = await self.create_commit(tree, head["sha"], message)
        await self.update_ref(sha, branch)
        url = f"https://github.com/{self._repo}/commit/{sha}"
        log.info(f"[GH] Pushed {len(files)} files: {url}")
        return url

    # ── PULL REQUESTS ─────────────────────────

    async def create_pr(self, title: str, body: str, head: str, base: str = "main") -> dict:
        r = await self._http.post(
            f"/repos/{self._repo}/pulls",
            json={"title": title, "body": body, "head": head, "base": base}
        )
        r.raise_for_status()
        return r.json()

    # ── ISSUES ────────────────────────────────

    async def create_issue(self, title: str, body: str, labels: list = None) -> dict:
        r = await self._http.post(
            f"/repos/{self._repo}/issues",
            json={"title": title, "body": body, "labels": labels or []}
        )
        r.raise_for_status()
        return r.json()

    # ── REPO INFO ─────────────────────────────

    async def repo_info(self) -> dict:
        r = await self._http.get(f"/repos/{self._repo}")
        if r.status_code == 200:
            d = r.json()
            return {
                "name":       d["name"],
                "stars":      d["stargazers_count"],
                "forks":      d["forks_count"],
                "open_issues":d["open_issues_count"],
                "url":        d["html_url"],
                "updated":    d["updated_at"],
                "default_branch": d["default_branch"],
            }
        return {"error": f"HTTP {r.status_code}"}

    async def close(self):
        await self._http.aclose()


