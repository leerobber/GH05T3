def create_github_webhook_router():
    """
    Returns a FastAPI APIRouter that receives GitHub webhooks
    and publishes them to the SwarmBus.
    Mount at /webhooks/github.
    """
    from fastapi import APIRouter, Request, Header, HTTPException
    import hmac, hashlib

    router = APIRouter(prefix="/webhooks/github")
    bus    = SwarmBus.instance()
    secret = os.environ.get("GITHUB_WEBHOOK_SECRET", "").encode()

    def _verify(body: bytes, sig: str) -> bool:
        if not secret:
            return True   # No secret configured — accept all
        expected = "sha256=" + hmac.new(secret, body, hashlib.sha256).hexdigest()
        return hmac.compare_digest(expected, sig or "")

    @router.post("")
    async def receive_webhook(
        request: Request,
        x_github_event: str = Header(default=""),
        x_hub_signature_256: str = Header(default=""),
    ):
        body = await request.body()
        if not _verify(body, x_hub_signature_256):
            raise HTTPException(403, "Invalid webhook signature")

        payload = json.loads(body)
        event   = x_github_event

        # Map events to human-readable messages
        if event == "push":
            pusher = payload.get("pusher", {}).get("name", "unknown")
            ref    = payload.get("ref", "")
            count  = len(payload.get("commits", []))
            content = f"📦 Push by {pusher} → {ref} ({count} commits)"
        elif event == "pull_request":
            action = payload.get("action", "")
            title  = payload.get("pull_request", {}).get("title", "")
            content = f"🔀 PR {action}: {title}"
        elif event == "issues":
            action = payload.get("action", "")
            title  = payload.get("issue", {}).get("title", "")
            content = f"🐛 Issue {action}: {title}"
        elif event == "release":
            name = payload.get("release", {}).get("name", "")
            content = f"🚀 Release: {name}"
        else:
            content = f"GitHub event: {event}"

        await bus.emit(
            src="GITHUB-WEBHOOK",
            content=content,
            channel="#github",
            msg_type=MsgType.GITHUB,
            gh_event=event,
            payload=payload,
        )

        return {"ok": True, "event": event}

    return router
